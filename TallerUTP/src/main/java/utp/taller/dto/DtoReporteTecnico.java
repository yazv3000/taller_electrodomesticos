package utp.taller.dto;

public class DtoReporteTecnico {

	private int id;
	private String nomTecnico;
	private String electrodomestico;
	private String servicio;
	private double monto;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNomTecnico() {
		return nomTecnico;
	}
	public void setNomTecnico(String nomTecnico) {
		this.nomTecnico = nomTecnico;
	}
	public String getElectrodomestico() {
		return electrodomestico;
	}
	public void setElectrodomestico(String electrodomestico) {
		this.electrodomestico = electrodomestico;
	}
	public String getServicio() {
		return servicio;
	}
	public void setServicio(String servicio) {
		this.servicio = servicio;
	}
	public double getMonto() {
		return monto;
	}
	public void setMonto(double monto) {
		this.monto = monto;
	}
	
	
	
	
}
